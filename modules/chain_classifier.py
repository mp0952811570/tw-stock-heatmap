def classify_stock(stock_name: str, industry_category: str) -> str:
    """
    根據股票名稱 + 產業別，推測上中下游分類。
    回傳: "上游", "中游", "下游", or "其他"
    """
    if not stock_name:
        return "其他"

    rules = CHAIN_KEYWORDS.get(industry_category)

    # 有專屬規則 → 用專屬規則
    if rules:
        for chain_type in ["上游", "中游", "下游"]:
            kw_set = rules[chain_type]["keywords"]
            for kw in kw_set:
                if kw in stock_name:
                    return chain_type

    # generic 規則
    for chain_type in ["上游", "中游", "下游"]:
        for kw in GENERIC_RULES[chain_type]["keywords"]:
            if kw in stock_name:
                return chain_type

    return "其他"


def classify_industry_stocks(stocks: list, industry_category: str) -> dict:
    """
    把一個產業的所有個股分配到上中下游。
    回傳: {
        "上游": [{id, name, type}, ...],
        "中游": [...],
        "下游": [...],
        "其他": [...],
    }
    """
    result = {"上游": [], "中游": [], "下游": [], "其他": []}
    for s in stocks:
        chain = classify_stock(s["name"], industry_category)
        result[chain].append({"id": s["id"], "name": s["name"], "type": s["type"]})
    return result
